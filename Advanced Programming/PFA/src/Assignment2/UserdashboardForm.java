    private void btn_enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enterActionPerformed
       try {
    // Parse the input values
    double currentSavings = Double.parseDouble(txt_saving.getText());
    double monthlyContribution = Double.parseDouble(txt_monthly.getText());
    double targetAmount = Double.parseDouble(txt_amount.getText());
    String item = txt_item.getText();  // Get the item name from the txt_item field

    // Calculate the number of months required
    if (monthlyContribution > 0) {
        double monthsRequired = (targetAmount - currentSavings) / monthlyContribution;
        String result;

        if (monthsRequired <= 0) {
            result = "You already have enough savings for " + item + "!";
        } else {
            // Display months and years if applicable
            result = "You will be able to buy your " + item + " in " + Math.ceil(monthsRequired) + " month(s)";
            
            // If more than 12 months, also display in years
            if (monthsRequired > 12) {
                double yearsRequired = monthsRequired / 12;
                result += " (" + Math.ceil(yearsRequired) + " year(s))";
            }
        }

        txt_result.setText(result);

        // Write all inputs and the result to the file
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("btn_add.txt", true))) {
            writer.write("Item: " + item);
            writer.newLine();
            writer.write("Current Savings: RM " + currentSavings);
            writer.newLine();
            writer.write("Monthly Contribution: RM " + monthlyContribution);
            writer.newLine();
            writer.write("Target Amount: RM " + targetAmount);
            writer.newLine();
            writer.write(result);  // Write the calculated result to the file
            writer.newLine();  // Add a new line for separation
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error saving to file: " + ex.getMessage());
        }
    } else {
        txt_result.setText("Monthly contribution must be greater than 0.");
    }
    } catch (NumberFormatException ex) {
    // Handle invalid input
    txt_result.setText("Please enter valid numbers.");
    }
    }//GEN-LAST:event_btn_enterActionPerformed

