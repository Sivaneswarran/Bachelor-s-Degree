    private void btn_eraseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eraseActionPerformed
    // Get the model for table2
    DefaultTableModel tblModel = (DefaultTableModel) table2.getModel();

    // Check if a row is selected in table2
    int selectedRow = table2.getSelectedRow();

    if (selectedRow != -1) {
    try {
        // Identify the row to be deleted
        String dataToDelete = (String) tblModel.getValueAt(selectedRow, 0);  // Assume first column contains the unique identifier
        tblModel.removeRow(selectedRow);

        // Read the file and rewrite it without the deleted data
        File inputFile = new File("btn_add.txt");
        BufferedReader reader = new BufferedReader(new FileReader(inputFile));
        StringBuilder updatedContent = new StringBuilder();

        String currentLine;
        while ((currentLine = reader.readLine()) != null) {
            if (!currentLine.startsWith(dataToDelete + ",")) {
                updatedContent.append(currentLine).append(System.lineSeparator());
            }
        }

        reader.close();

        // Write the updated content back to the same file
        FileWriter writer = new FileWriter(inputFile);
        writer.write(updatedContent.toString());
        writer.close();

        JOptionPane.showMessageDialog(this, "Row deleted from Table and file successfully.");
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    } else {
    // Handle cases where no rows are selected
    if (table2.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Table is empty.");
    } else {
        JOptionPane.showMessageDialog(this, "Please select a row from Table for deletion.");
    }
    }
    }//GEN-LAST:event_btn_eraseActionPerformed

    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
       // Check if any field is empty
    String name = txt_name.getText().trim();
    String incomeText = txt_income.getText().trim();
    String expensesText = txt_expenses.getText().trim();

    if (name.isEmpty() || incomeText.isEmpty() || expensesText.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Oops! You missed something! Fill out all fields to keep the rizz!");
    } else {
    try {
        // Parse the input values
        int income = Integer.parseInt(incomeText);
        int expenses = Integer.parseInt(expensesText);
        
        // Display the values in lbl_income and lbl_expenses
        lbl_income.setText("RM" + " " + income);
        lbl_expenses.setText("RM" + " " + expenses);

        // Calculate net balance
        int netBalance = income - expenses;

        // Create a pie chart dataset
        lbl_netbalance.setText("RM" + " " + netBalance);
        DefaultPieDataset pieDataset = new DefaultPieDataset();
        pieDataset.setValue("Income", income);
        pieDataset.setValue("Expenses", expenses);
        pieDataset.setValue("Net Balance", netBalance);

        // Create the pie chart
        JFreeChart chart = ChartFactory.createPieChart("Statistical Data", pieDataset);
        PiePlot plot = (PiePlot) chart.getPlot();

        // Display the chart in a ChartFrame
        ChartFrame frame = new ChartFrame("Statistical Data", chart);
        frame.setVisible(true);
        frame.setSize(516, 306);

        // Prepare the result string (no labels)
        String result = name + "," + income + "," + expenses + "," + netBalance;

        // Write the data to the file
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("btn_add.txt", true))) {
            writer.write(result);
            writer.newLine();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error saving to file: " + ex.getMessage());
        }

        // Insert the data at the top of the JTable (table2)
        DefaultTableModel model = (DefaultTableModel) table2.getModel();
        model.insertRow(0, new Object[]{name, + income, + expenses, + netBalance});

        // Success message
        JOptionPane.showMessageDialog(null, "Naise! Your record’s locked in, and you've scored some aura points!");

        // Clear the text fields after adding the data
        txt_name.setText("");
        txt_income.setText("");
        txt_expenses.setText("");

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Please enter valid numbers for income and expenses.");
    }
    }
    }//GEN-LAST:event_btn_submitActionPerformed

