    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // Check if username or password fields are empty
        String username = txt_username3.getText();
        String password = txt_password3.getText();
        
        try {
        // Writing to the file
        FileWriter writer = new FileWriter("btn_add.txt", true);
        writer.write(username + "," + password);
        writer.write(System.getProperty("line.separator")); // Adds a new line
        writer.close();
    
        // You can add additional logic here if needed, such as clearing input fields or updating a UI component
        } catch (IOException e) {
        System.err.println("Error: " + e.getMessage()); // Log the error in the console
        }

        if (txt_username3.getText().isEmpty() || txt_password3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Oops! You missed something! Fill out both the username and password to keep the rizz!");
        } else {
            // Get the table model for both tables
            DefaultTableModel tblModel1 = (DefaultTableModel) table.getModel();
            DefaultTableModel tblModel2 = (DefaultTableModel) table1.getModel();

            // Add username and password to both tables
            tblModel1.insertRow(0, new Object[] {
                txt_username3.getText(),
                txt_password3.getText()
            });
            tblModel2.insertRow(0, new Object[] {
                txt_username3.getText(),
                txt_password3.getText()
            });

            JOptionPane.showMessageDialog(null, "Naise! Your record’s locked in, and you've scored some aura points, with all data successfully added to both profiles!");

            // Clear the text fields after adding the data
            txt_username3.setText("");
            txt_password3.setText("");
        } 
    }//GEN-LAST:event_btn_addActionPerformed

