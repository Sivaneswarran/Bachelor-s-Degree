    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // Retrieve new values from input fields
    String newUsername = txt_username3.getText();
    String newPassword = txt_password3.getText();

    // Get models for both tables
    DefaultTableModel tblModel1 = (DefaultTableModel) table.getModel();
    DefaultTableModel tblModel2 = (DefaultTableModel) table1.getModel();

    // Check if a row is selected in table
    int selectedRow = table.getSelectedRow();
    int selectedRow1 = table1.getSelectedRow();

    if (selectedRow != -1 || selectedRow1 != -1) {
    try {
        String oldUsername;

        // Update Table
        if (selectedRow != -1) {
            oldUsername = (String) tblModel1.getValueAt(selectedRow, 0);
            tblModel1.setValueAt(newUsername, selectedRow, 0);
            tblModel1.setValueAt(newPassword, selectedRow, 1);
            tblModel2.setValueAt(newUsername, selectedRow, 0); // Sync table1 too
            tblModel2.setValueAt(newPassword, selectedRow, 1);
        } else {
            oldUsername = (String) tblModel2.getValueAt(selectedRow1, 0);
            tblModel2.setValueAt(newUsername, selectedRow1, 0);
            tblModel2.setValueAt(newPassword, selectedRow1, 1);
            tblModel1.setValueAt(newUsername, selectedRow1, 0); // Sync table too
            tblModel1.setValueAt(newPassword, selectedRow1, 1);
        }

        // Update the file
        File inputFile = new File("btn_add.txt");
        File tempFile = new File("temp_btn_add.txt");

        BufferedReader reader = new BufferedReader(new FileReader(inputFile));
        FileWriter writer = new FileWriter(tempFile);

        String currentLine;
        while ((currentLine = reader.readLine()) != null) {
            if (currentLine.startsWith(oldUsername + ",")) {
                writer.write(newUsername + "," + newPassword + System.lineSeparator());
            } else {
                writer.write(currentLine + System.lineSeparator());
            }
        }

        reader.close();
        writer.close();

        // Replace the original file with the updated file
        if (inputFile.delete() && tempFile.renameTo(inputFile)) {
            JOptionPane.showMessageDialog(this, "Update Successful in Tables and File!");
        } else {
            JOptionPane.showMessageDialog(this, "Error updating file. Please try again.");
        }

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    } else {
    // Handle cases where no rows are selected
    if (table.getRowCount() == 0 && table1.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Both tables are empty.");
    } else {
        JOptionPane.showMessageDialog(this, "Please select a single row from the Table for update.");
    }
    }
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // Get models for both tables
        DefaultTableModel tblModel1 = (DefaultTableModel) table.getModel();
        DefaultTableModel tblModel2 = (DefaultTableModel) table1.getModel();

        // Check if a row is selected in either table
        int selectedRow = table.getSelectedRow();
        int selectedRow1 = table1.getSelectedRow();

        if (selectedRow != -1 || selectedRow1 != -1) {
        
            try {
        // Identify the row to be deleted (prioritize table)
        String usernameToDelete;
        if (selectedRow != -1) {
            usernameToDelete = (String) tblModel1.getValueAt(selectedRow, 0);
            tblModel1.removeRow(selectedRow);
            tblModel2.removeRow(selectedRow);
        } else {
            usernameToDelete = (String) tblModel2.getValueAt(selectedRow1, 0);
            tblModel1.removeRow(selectedRow1);
            tblModel2.removeRow(selectedRow1);
        }

        // Read the file and rewrite it without the deleted username
        File inputFile = new File("btn_add.txt");
        File tempFile = new File("temp_btn_add.txt");

        BufferedReader reader = new BufferedReader(new FileReader(inputFile));
        FileWriter writer = new FileWriter(tempFile);

        String currentLine;
        while ((currentLine = reader.readLine()) != null) {
            if (!currentLine.startsWith(usernameToDelete + ",")) {
                writer.write(currentLine + System.lineSeparator());
            }
        }

        reader.close();
        writer.close();

        // Replace the original file with the updated file
        if (inputFile.delete() && tempFile.renameTo(inputFile)) {
            JOptionPane.showMessageDialog(this, "Row deleted from Table, Table1, and file.");
        } else {
            JOptionPane.showMessageDialog(this, "Error updating file. Please try again.");
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    } 
        else {
    // Handle cases where no rows are selected
    if (table.getRowCount() == 0 && table1.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Both tables are empty.");
    } else {
        JOptionPane.showMessageDialog(this, "Please select a single row from the Table for deletion.");
    }
    }
    }//GEN-LAST:event_btn_deleteActionPerformed

